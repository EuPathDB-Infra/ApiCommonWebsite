CREATE TABLE ${USER_SCHEMA_KEY}OPENID_USERS (
    "USERID"     NUMBER PRIMARY KEY,
    "OPENID_URL" VARCHAR2(128 BYTE) NOT NULL
);

CREATE TABLE ${USER_SCHEMA_KEY}SESSION_TBL (
    "USERID"    NUMBER PRIMARY KEY,
    "UPLOADSID" VARCHAR2(32 BYTE),
    "SESSIONID" VARCHAR2(32 BYTE),
    "USERNAME"  VARCHAR2(32 BYTE)
);

CREATE TABLE ${USER_SCHEMA_KEY}SESSIONS (
    "ID" VARCHAR2(32 BYTE) PRIMARY KEY,
    "A_SESSION" LONG NOT NULL
);

CREATE TABLE ${USER_SCHEMA_KEY}SHARING (
    "USERID"    NUMBER,
    "IS_PUBLIC" NUMBER,
    "TRACKID"   VARCHAR2(32 BYTE) PRIMARY KEY
);

CREATE TABLE ${USER_SCHEMA_KEY}UPLOADS (
    "USERID"         NUMBER,
    "SHARING_POLICY" VARCHAR2(36 BYTE),
    "DESCRIPTION" CLOB,
    "TRACKID" VARCHAR2(32 BYTE) PRIMARY KEY,
    "MODIFICATION_DATE" DATE,
    "CREATION_DATE" DATE,
    "PUBLIC_COUNT" NUMBER,
    "IMPORTED"     NUMBER,
    "PATH"         VARCHAR2(1024 BYTE),
    "DATA_SOURCE"  VARCHAR2(1024 BYTE),
    "TITLE"        VARCHAR2(1024 BYTE)
);

CREATE TABLE ${USER_SCHEMA_KEY}USERS (
    "LAST_LOGIN" DATE,
    "PASS"     VARCHAR2(50 BYTE),
    "USERID"   NUMBER PRIMARY KEY,
    "REMEMBER" NUMBER,
    "CREATED" DATE,
    "EMAIL"       VARCHAR2(64 BYTE),
    "OPENID_ONLY" NUMBER,
    "CONFIRMED"   NUMBER,
    "GECOS"       VARCHAR2(64 BYTE),
    "CNFRM_CODE"  VARCHAR2(32 BYTE)
);

CREATE SEQUENCE ${USER_SCHEMA_KEY}GBROWSE_UID_SEQ
    MINVALUE 1 MAXVALUE 9999999999999999999999999999
    INCREMENT BY 1 START WITH 1
    NOCACHE NOORDER NOCYCLE;
